from flask import Flask, jsonify, render_template, request

app = Flask(__name__)

# HOME PAGE
@app.route('/')
def home():
    return render_template('index.html')

# API DATA
@app.route('/api/data')
def get_data():

    data = {
        "name": "Ebony",
        "role": "Developer",
        "language": "Python"
    }

    return jsonify(data)

# CHATBOT API
@app.route('/chat', methods=['POST'])
def chat():

    user_message = request.json.get('message')

    if user_message.lower() == 'hello':
        reply = "Hello Ebony!"

    elif user_message.lower() == 'how are you':
        reply = "I'm doing great!"

    else:
        reply = "You said: " + user_message

    return jsonify({
        "ai": reply
    })

if __name__ == '__main__':
    app.run(debug=True)
























































































































































































































































































































